#include "libft.h"

t_list	*ft_lstnew(long content)
{
	t_list	*newlist;

	newlist = malloc(sizeof(t_list));
	if (!newlist)
		return (NULL);
	newlist->content = content;
	newlist->higher = 0;
	newlist->wait = 0;
	newlist->next = NULL;
	return (newlist);
}
