#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	long int	nbr;
	int			d;

	nbr = n;
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
	}
	d = nbr % 10 + '0';
	write(fd, &d, 1);
}
