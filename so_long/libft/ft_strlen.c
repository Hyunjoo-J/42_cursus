#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

size_t	ft_strlen2d(char **s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
